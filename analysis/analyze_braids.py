# census
# ---------------------------------------------------------------------------


@dataclass
class DumpResult:
    t: int
    seed: int
    size_hist: Counter = field(default_factory=Counter)
    groups: list[dict] = field(default_factory=list)
    skipped_giant: int = 0


def analyze_dump(
    path: str, supersample: int, max_group: int, b3dict: dict
) -> DumpResult:
    m = _NAME_RE.search(Path(path).name)
    if not m:
        raise ValueError(f"cannot parse T/seed from {path}")
    t, seed = int(m["t"]), int(m["seed"])
    strands = load_dump(path)
    by_gid: dict[int, list[Strand]] = defaultdict(list)
    for s in strands:
        by_gid[s.gid].append(s)

    # Engine z-grid [pi/(T+1), T*pi/(T+1)], supersampled for crossing capture.
    zgrid = np.linspace(pi / (t + 1), t * pi / (t + 1), (t - 1) * supersample + 1)
    contact = 2.0 / t

    res = DumpResult(t=t, seed=seed)
    for gid, members in by_gid.items():
        res.size_hist[len(members)] += 1
        if len(members) < 2:
            continue
        if len(members) > max_group:
            res.skipped_giant += 1
            continue
        gb = group_braid(members, zgrid, contact)
        row = {
            "t": t,
            "seed": seed,
            "gid": gid,
            "n": gb.n,
            "n_crossings": len(gb.word),
            "exp_sum": gb.exp_sum,
            "perm": "".join(map(str, gb.perm)),
            "fragile": gb.fragile,
            "anomalies": gb.anomalies,
            "class": "",
            "class_key": "",
            "class_len": "",
        }
        if gb.n == 3:
            key = b3_invariant(gb.word)
            short = b3dict.get(key)
            row["class_key"] = str(key)
            if short is not None:
                row["class"] = word_str(short)
                row["class_len"] = len(short)
            else:
                # Too deep for the dictionary: bucket the display label by
                # exponent sum (the exact invariant stays in class_key).
                row["class"] = f"deep (e={gb.exp_sum:+d})"
                row["class_len"] = -1
        elif gb.n == 2:
            e = gb.exp_sum
            if e == 0:
                row["class"] = "1 (trivial)"
            elif abs(e) == 1:
                row["class"] = "s1" if e > 0 else "s1'"
            else:
                row["class"] = f"s1^{e}"
            row["class_key"] = str(e)
            row["class_len"] = abs(e)
        res.groups.append(row)
    return res


def group_is_braided(g: dict) -> bool:
    """Nontrivial-braid test: exact for n<=3, exp-sum lower bound for n>=4."""
    if g["class"]:
        return g["class"] != "1 (trivial)"
    return g["exp_sum"] != 0


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--dumps",
        default="data/converge/dumps/d2_*_sub_*.csv",
        help="glob of 2+1 subpath dumps (need a gid column)",
    )
    parser.add_argument("--out", type=Path, default=Path("figures/braids.png"))
    parser.add_argument(
        "--csv",
        type=Path,
        default=Path("figures/braids_groups.csv"),
        help="per-group classification table",
    )
    parser.add_argument(
        "--supersample",
        type=int,
        default=8,
        help="z samples per engine step for crossing capture",
    )
    parser.add_argument(
        "--max-group",
        type=int,
        default=48,
        help="skip braid extraction above this group size",
    )
    parser.add_argument(
        "--dict-len", type=int, default=6, help="B_3 shortest-word dictionary depth"
    )
    args = parser.parse_args()

    paths = sorted(glob.glob(args.dumps))
    if not paths:
        raise SystemExit(f"no dumps match {args.dumps}")
    b3dict = build_b3_dictionary(args.dict_len)
    print(f"B_3 dictionary: {len(b3dict)} classes up to word length {args.dict_len}")

    results = [analyze_dump(p, args.supersample, args.max_group, b3dict) for p in paths]
    for r, p in zip(results, paths):
        n_groups = sum(1 for _ in r.groups)
        print(
            f"  {Path(p).name}: {n_groups} multi-strand groups analyzed"
            + (f" ({r.skipped_giant} giant skipped)" if r.skipped_giant else "")
        )

    rows = [g for r in results for g in r.groups]
    skipped = sum(r.skipped_giant for r in results)
    if skipped:
        print(
            f"NOTE: {skipped} groups larger than --max-group={args.max_group} "
            "were skipped for braid extraction (still in the size census)."
        )

    if not rows:
        raise SystemExit("no multi-strand groups found in the given dumps")
    args.csv.parent.mkdir(parents=True, exist_ok=True)
    with open(args.csv, "w", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)
    print(f"wrote {args.csv} ({len(rows)} groups)")

    # ---- printed census ----
    pairs = [g for g in rows if g["n"] == 2]
    triples = [g for g in rows if g["n"] == 3]
    braided_pairs = [g for g in pairs if g["exp_sum"] != 0]
    solid_triples = [g for g in triples if g["fragile"] == 0]
    print(f"\n=== census over {len(paths)} dumps ===")
    print(
        f"pairs (n=2): {len(pairs)}, with nonzero linking: {len(braided_pairs)} "
        f"({100 * len(braided_pairs) / max(len(pairs), 1):.1f}%)"
    )
    print(f"triples (n=3): {len(triples)}, fragile-free: {len(solid_triples)}")
    for label, subset in (("all triples", triples), ("fragile-free", solid_triples)):
        census = Counter(g["class"] for g in subset)
        print(f"\n  B_3 classes ({label}):")
        for cls, cnt in census.most_common(15):
            print(f"    {cls:<24} {cnt:>5}  ({100 * cnt / max(len(subset), 1):.1f}%)")

    # Chirality balance: does jamming prefer a handedness? (parity test)
    pe_pos = sum(1 for g in pairs if g["exp_sum"] > 0)
    pe_neg = sum(1 for g in pairs if g["exp_sum"] < 0)
    te_pos = sum(1 for g in triples if g["exp_sum"] > 0)
    te_neg = sum(1 for g in triples if g["exp_sum"] < 0)
    print(
        f"\n  chirality balance (exp_sum sign): pairs +{pe_pos}/-{pe_neg}, "
        f"triples +{te_pos}/-{te_neg}"
    )

    nontrivial = Counter()
    total_by_n = Counter()
    for g in rows:
        total_by_n[g["n"]] += 1
        if group_is_braided(g):
            nontrivial[g["n"]] += 1
    print("\n  braided fraction by group size (n>=4: exp-sum lower bound):")
    for n in sorted(total_by_n):
        if n > 12:
            break
        print(
            f"    n={n}: {nontrivial[n]}/{total_by_n[n]} "
            f"({100 * nontrivial[n] / total_by_n[n]:.0f}%)"
        )

    frag = sum(g["fragile"] for g in rows)
    total_x = sum(g["n_crossings"] for g in rows)
    anom = sum(g["anomalies"] for g in rows)
    print(
        f"\n  crossings: {total_x} total, {frag} fragile "
        f"({100 * frag / max(total_x, 1):.1f}%), {anom} order anomalies"
    )

    make_figure(results, rows, args.out)


def make_figure(results: list[DumpResult], rows: list[dict], out: Path) -> None:
    import matplotlib

    matplotlib.use("Agg")
    import matplotlib.pyplot as plt

    fig, axes = plt.subplots(1, 4, figsize=(21, 5))

    # (a) group-size distribution by T (sequential colormap: T is ordered)
    ax = axes[0]
    by_t: dict[int, Counter] = defaultdict(Counter)
    for r in results:
        by_t[r.t].update(r.size_hist)
    ts = sorted(by_t)
    cmap = plt.colormaps["viridis"]
    for idx, t in enumerate(ts):
        sizes = sorted(by_t[t])
        counts = [by_t[t][s] for s in sizes]
        ax.loglog(
            sizes,
            counts,
            "o-",
            ms=3,
            lw=1,
            color=cmap(idx / max(len(ts) - 1, 1)),
            label=f"T={t}",
        )
    ax.set_xlabel("group size n (strands)")
    ax.set_ylabel("groups (all seeds)")
    ax.set_title("(a) unique-group size distribution")
    ax.legend(fontsize=7)
    ax.grid(True, which="both", alpha=0.25)

    # (b) pair (n=2) linking: exponent-sum distribution
    ax = axes[1]
    es = [g["exp_sum"] for g in rows if g["n"] == 2]
    lo, hi = min(es, default=-1), max(es, default=1)
    bins = np.arange(lo - 0.5, hi + 1.5)
    ax.hist(es, bins=bins, color="tab:blue", edgecolor="white")
    ax.set_xlabel("signed crossing sum e (B$_2$ class)")
    ax.set_ylabel("pairs")
    ax.set_title("(b) 2-strand groups: linking census")
    ax.grid(True, axis="y", alpha=0.25)

    # (c) 3-strand class census, Bilson-Thompson template highlighted
    ax = axes[2]
    solid = [g for g in rows if g["n"] == 3 and g["fragile"] == 0]
    census = Counter(g["class"] for g in solid)
    top = census.most_common(12)
    labels = [c for c, _ in top]
    counts = [v for _, v in top]
    bt_like = {"s1.s2'", "s2.s1'", "s1'.s2", "s2'.s1"}  # sigma_i sigma_j^-1 templates
    colors = ["tab:orange" if lb in bt_like else "tab:blue" for lb in labels]
    ax.barh(range(len(top))[::-1], counts, color=colors, edgecolor="white")
    ax.set_yticks(range(len(top))[::-1])
    ax.set_yticklabels(labels, fontsize=8, family="monospace")
    ax.set_xlabel("groups (fragile-free)")
    ax.set_title(
        "(c) B$_3$ classes of 3-strand groups\n"
        "(orange = Bilson-Thompson $\\sigma_i\\sigma_j^{-1}$ template)"
    )
    ax.grid(True, axis="x", alpha=0.25)

    # (d) braided fraction vs group size
    ax = axes[3]
    total_by_n = Counter()
    braided_by_n = Counter()
    for g in rows:
        total_by_n[g["n"]] += 1
        if group_is_braided(g):
            braided_by_n[g["n"]] += 1
    ns = [n for n in sorted(total_by_n) if total_by_n[n] >= 5]
    frac = [braided_by_n[n] / total_by_n[n] for n in ns]
    ax.plot(ns, frac, "o-", color="tab:blue")
    ax.set_xlabel("group size n")
    ax.set_ylabel("fraction with nontrivial braid")
    ax.set_ylim(0, 1.05)
    ax.set_title("(d) does touching imply braiding?")
    ax.grid(True, alpha=0.25)

    fig.tight_layout()
    out.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(out, dpi=130)
    print(f"wrote {out}")


if __name__ == "__main__":
    main()
